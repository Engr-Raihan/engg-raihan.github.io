const CryptoJS = require("crypto-js");

const key = CryptoJS.enc.Utf8.parse('1234567890123456'); // 128-bit key
const iv = CryptoJS.enc.Utf8.parse('1234567890123456'); // 128-bit IV

function encryptData(data) {
    const encrypted = CryptoJS.AES.encrypt(data, key, { iv: iv });
    return encrypted.toString();
}

function decryptData(encryptedData) {
    const decrypted = CryptoJS.AES.decrypt(encryptedData, key, { iv: iv });
    return decrypted.toString(CryptoJS.enc.Utf8);
}

// Example usage
const encrypted = encryptData('Hello World');
console.log('Encrypted:', encrypted);
const decrypted = decryptData(encrypted);
console.log('Decrypted:', decrypted);
